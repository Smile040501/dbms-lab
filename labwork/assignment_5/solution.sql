/* Q2: Create a function that consumes an ‘ID’ number as an argument and returns the complete details of the customer corresponding to that particular ID ranging from the customer name (full), their address ID, and the amount paid by them including the payment date.
 */
CREATE OR REPLACE FUNCTION get_customer_info(cid INT)
RETURNS TABLE(
        first_name VARCHAR,
        last_name VARCHAR,
        address_id SMALLINT,
        amount NUMERIC,
        payment_date TIMESTAMP
    )
LANGUAGE PLPGSQL
AS $$
BEGIN
    RETURN QUERY
        SELECT customer.first_name,
            customer.last_name,
            customer.address_id,
            payment.amount,
            payment.payment_date
        FROM customer
            INNER JOIN payment ON customer.customer_id = payment.customer_id
            AND customer.customer_id = cid;
END;
$$;

