/* Q4: Create a procedure that displays all the film IDs in ascending order and the name (title) of each film corresponding to that particular ID
 */
CREATE OR REPLACE PROCEDURE display_films()
LANGUAGE PLPGSQL
AS $$
DECLARE
    rec RECORD;
BEGIN
    FOR rec IN (
                SELECT film_id,
                    title
                FROM film
                ORDER BY film_id ASC
            )
    LOOP
        RAISE NOTICE 'film_id: %, title: %', rec.film_id, rec.title;
    END LOOP;
END;
$$;
